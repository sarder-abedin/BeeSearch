# ── create_notebook_state ─────────────────────────────────────────────────────

class TestCreateNotebookState:
    def test_required_fields_present(self):
        state = create_notebook_state(user_message="What is X?", notebook_id="nb1")
        for key in ("user_message", "notebook_id", "model_name", "embed_model",
                    "top_k", "retrieved_chunks", "citations", "completed_steps"):
            assert key in state

    def test_user_message_and_notebook_stored(self):
        state = create_notebook_state(user_message="Hello?", notebook_id="nb42")
        assert state["user_message"] == "Hello?"
        assert state["notebook_id"] == "nb42"

    def test_defaults(self):
        state = create_notebook_state(user_message="q", notebook_id="nb1")
        assert state["retrieved_chunks"] == []
        assert state["citations"] == []
        assert state["suggested_questions"] == []
        assert state["retrieval_mode"] == "empty"
        assert state["completed_steps"] == []
        assert state["progress_pct"] == 0

    def test_top_k_and_embed_model_passed_through(self):
        state = create_notebook_state(
            user_message="q", notebook_id="nb1",
            embed_model="bge-m3", top_k=12,
        )
        assert state["embed_model"] == "bge-m3"
        assert state["top_k"] == 12
