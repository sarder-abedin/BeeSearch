# ── create_initial_state ──────────────────────────────────────────────────────

class TestCreateInitialState:
    def test_required_fields_present(self):
        state = create_initial_state(goal="test goal", mode="search")
        for key in ("goal", "mode", "session_id", "clarifications", "errors", "completed_steps"):
            assert key in state

    def test_goal_stored(self):
        state = create_initial_state(goal="My Research Goal", mode="search")
        assert state["goal"] == "My Research Goal"

    def test_mode_stored(self):
        for mode in ("document", "search", "hybrid"):
            state = create_initial_state(goal="g", mode=mode)
            assert state["mode"] == mode

    def test_clarifications_default_empty(self):
        state = create_initial_state(goal="g", mode="search")
        assert state["clarifications"] == {}

    def test_clarifications_passed_through(self):
        clarifs = {"audience": "researchers", "depth": "comprehensive"}
        state = create_initial_state(goal="g", mode="search", clarifications=clarifs)
        assert state["clarifications"] == clarifs

    def test_errors_starts_empty(self):
        state = create_initial_state(goal="g", mode="search")
        assert state["errors"] == []

    def test_session_id_autogenerated_if_omitted(self):
        state = create_initial_state(goal="g", mode="search")
        assert isinstance(state["session_id"], str) and state["session_id"]


